package javafxpaginationfxml;

/**
 *
 * @author rifat
 */
public class Sample {

    public  int  id;
    public String foo;
    public String bar;

    public Sample(int id, String foo, String bar) {
        this.id = id;
        this.foo = foo;
        this.bar = bar;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFoo() {
        return foo;
    }

    public void setFoo(String foo) {
        this.foo = foo;
    }

    public String getBar() {
        return bar;
    }

    public void setBar(String bar) {
        this.bar = bar;
    }

    
    
    

}
